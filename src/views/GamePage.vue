<template>
  <div class="game-page">
    <!-- Back to Home and Steam Button Row -->
    <div class="navigation-row">
      <Button @click="goBack" aria-label="Go back to home" size="small">
        ‚Üê Back to Search
      </Button>
      <UserProfile :user="user" />
    </div>

    <!-- Loading State -->
    <Spinner v-if="loading" message="Searching for game settings..." />

    <!-- Error State -->
    <ErrorMessage :message="error" @dismiss="clearError" class="error-with-top-margin" />

    <!-- Game Description Section -->
    <GameDescription :game="game" :loading="loading" />

    <!-- <GameSettings id="settings-section" :game="game" :user="user" :loading="loading" :error="error"
      :search-performed="searchPerformed" :processing-warning="processingWarning"
      @clear-processing-warning="clearProcessingWarning" @submit-vote="handleVoteSubmit" /> -->

    <AskAICard 
      v-if="game" 
      class="settings-section" 
      :title="`What is the community saying about this game?`"
      :animate-text="true"
      :text-content="aiCardContent"
      :type-speed="30"
      :start-delay="800"
    >
    </AskAICard>
        

    <!-- Game Reports Section -->
    <GameReportsSection v-if="game && game.reports" :reports="game.reports" />

    <GameDataSources class="game-data-sources" :game="game" />

    <!-- Processing game view -->
    <ProcessingWarning v-if="processingWarning" :game-name="gameTitle" @dismiss="clearProcessingWarning" />
    <RandomArt v-if="processingWarning" />
    <div v-if="processingWarning" style="margin-top: 24px; text-align: center;">
      <RefreshButton @refresh="handleRefresh" :countdown-start="60" />
    </div>
  </div>
</template>

<script>
import Button from '../components/base/Button.vue'
import Spinner from '../components/base/Spinner.vue'
import AskAICard from '../components/common/AskAICard.vue'
import ErrorMessage from '../components/common/ErrorMessage.vue'
import QuickLink from '../components/common/QuickLink.vue'
import RandomArt from '../components/common/RandomArt.vue'
import RefreshButton from '../components/common/RefreshButton.vue'
import GameDataSources from '../components/ui/GameDataSources.vue'
import GameDescription from '../components/ui/GameDescription.vue'
import GameReportsSection from '../components/ui/GameReportsSection.vue'
import GameSettings from '../components/ui/GameSettings.vue'
import ProcessingWarning from '../components/ui/ProcessingWarning.vue'
import ThumbsRating from '../components/ui/ThumbsRating.vue'
import UserProfile from '../components/ui/UserProfile.vue'
import apiService from '../services/backend/apiService.js'

export default {
  name: 'GamePage',
  components: {
    Button,
    QuickLink,
    GameSettings,
    GameDescription,
    GameReportsSection,
    ProcessingWarning,
    ErrorMessage,
    Spinner,
    GameDataSources,
    UserProfile,
    ThumbsRating,
    RefreshButton,
    RandomArt,
    AskAICard,
  },
  props: {
    gameId: {
      type: String,
      required: true,
    },
  },
  data() {
    return {
      game: null,
      loading: false,
      error: null,
      searchPerformed: false,
      processingWarning: false,
      user: null,
    }
  },
  computed: {
    gameTitle() {
      return this.game?.steam_app?.name || `Game ID ${this.gameId}`
    },
    aiCardContent() {
      if (!this.game?.game_performance_summary) return ''
      
      const summary = this.game.game_performance_summary
      const disclaimer = '\n\nThis is a brief summary of community feedback generated by AI and may contain errors.\nPlease refer to the community reports for accurate information.'
      
      return summary + disclaimer
    },
  },
  async mounted() {
    // Add keyboard event listener for backspace key
    document.addEventListener('keydown', this.handleKeydown)
    this.updateDocumentTitle()
    this.loadGame()
    this.user = await apiService.fetchAuthUser()
  },
  unmounted() {
    // Remove keyboard event listener to prevent memory leaks
    document.removeEventListener('keydown', this.handleKeydown)
  },
  methods: {
    async handleVoteSubmit(gameSettingsId, type) {
      if (type === null) {
        await apiService.removeVote(gameSettingsId)
      } else {
        await apiService.submitVote(gameSettingsId, type)
      }
      // Refresh user and game data to reflect updated votes
      this.user = await apiService.fetchAuthUser()
      this.game = (await apiService.fetchGame(this.gameId)).game
    },
    handleRefresh() {
      window.location.reload()
    },
    handleKeydown(event) {
      // Check if backspace key is pressed and not in an input field
      if (event.key === 'Backspace' && !this.isInInputField(event.target)) {
        event.preventDefault()
        this.goBack()
      }
    },

    isInInputField(target) {
      // Check if the target element is an input field where backspace should work normally
      const inputTypes = ['INPUT', 'TEXTAREA', 'SELECT']
      return inputTypes.includes(target.tagName) || target.contentEditable === 'true'
    },

    goBack() {
      // Preserve the search term from the current URL when going back
      const urlParams = new URLSearchParams(window.location.search)
      const searchQuery = urlParams.get('q')

      if (searchQuery) {
        this.$router.push({ name: 'Home', query: { q: searchQuery } })
      } else {
        this.$router.push({ name: 'Home' })
      }
    },

    clearError() {
      this.error = null
    },

    clearProcessingWarning() {
      this.processingWarning = false
    },

    updateDocumentTitle() {
      document.title = `${this.gameTitle} - Steam Deck Settings DB`
    },

    async loadGame() {
      if (!this.gameId) {
        this.error = 'Unknown game ID.'
        return
      }

      this.loading = true
      this.error = null
      this.game = null
      this.searchPerformed = true
      this.processingWarning = false

      try {
        const res = await apiService.fetchGame(this.gameId)

        if (res.status === 'queued') {
          this.processingWarning = true
          return
        }

        const sortedReports = this.sortGameReportsPerRelevance(res.game.reports)
        this.game = { ...res.game, reports: sortedReports }
      } catch (err) {
        this.error = err.message
      } finally {
        this.loading = false
      }
    },

    sortGameReportsPerRelevance(reports) {
      // Example sorting logic: prioritize reports from certain sources
      const sourcePriority = {
        steamdeckhq: 1,
        sharedeck: 2,
        protondb: 3,
      }

      return reports.sort((a, b) => {
        const priorityA = sourcePriority[a.source] || 99
        const priorityB = sourcePriority[b.source] || 99
        return priorityA - priorityB
      })
    },
  },
}
</script>

